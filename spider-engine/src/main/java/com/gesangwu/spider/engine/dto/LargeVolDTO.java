package com.gesangwu.spider.engine.dto;

public class LargeVolDTO {
	
	private String CODE;
	private int TRADE_TYPE;
	private double PRICE_PRE;
	private int VOLUME_INC;
	private String SYMBOL;
	private double PERCENT;
	private String NAME;
	private double PRICE;
	private double TURNOVER_INC;
	private String DATE;
	private String RN;
	
	public String getCODE() {
		return CODE;
	}
	public void setCODE(String cODE) {
		CODE = cODE;
	}
	public int getTRADE_TYPE() {
		return TRADE_TYPE;
	}
	public void setTRADE_TYPE(int tRADE_TYPE) {
		TRADE_TYPE = tRADE_TYPE;
	}
	public double getPRICE_PRE() {
		return PRICE_PRE;
	}
	public void setPRICE_PRE(double pRICE_PRE) {
		PRICE_PRE = pRICE_PRE;
	}
	public int getVOLUME_INC() {
		return VOLUME_INC;
	}
	public void setVOLUME_INC(int vOLUME_INC) {
		VOLUME_INC = vOLUME_INC;
	}
	public String getSYMBOL() {
		return SYMBOL;
	}
	public void setSYMBOL(String sYMBOL) {
		SYMBOL = sYMBOL;
	}
	public double getPERCENT() {
		return PERCENT;
	}
	public void setPERCENT(double pERCENT) {
		PERCENT = pERCENT;
	}
	public String getNAME() {
		return NAME;
	}
	public void setNAME(String nAME) {
		NAME = nAME;
	}
	public double getPRICE() {
		return PRICE;
	}
	public void setPRICE(double pRICE) {
		PRICE = pRICE;
	}
	public double getTURNOVER_INC() {
		return TURNOVER_INC;
	}
	public void setTURNOVER_INC(double tURNOVER_INC) {
		TURNOVER_INC = tURNOVER_INC;
	}
	public String getDATE() {
		return DATE;
	}
	public void setDATE(String dATE) {
		DATE = dATE;
	}
	public String getRN() {
		return RN;
	}
	public void setRN(String rN) {
		RN = rN;
	}
	
}
